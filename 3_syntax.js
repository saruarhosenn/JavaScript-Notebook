/* 
JavaScript values হচ্ছে ২ প্রকার। 
=====================================
১) Fixed values, এটাকে আবার Literals values বলা হয়। Fixed values বা Literals values হচ্ছে string, number। যেমনঃ 10, 10.10 & "Saruar Hosen"
নোটঃ এই values কোনো এক জায়গায় সরাসরি বসবে। এইগুলা কোনো Variable এ Assign করা হবে না। 
যেমনঃ 
console.log(10 + 10);
console.log("Saruar Hosen");
document.getElementById("demo").innerHTML = 10.50;

2) Variable values, Variables values এর কাজ হচ্ছে ডাটা জমা রাখা। var, let, const দিয়ে variables declare করা হয়। 
নোটঃ এখানে Variable values মানে হচ্ছে, কোনো একটা Variable নাম দিয়ে Variable declare করে। সেই Variable নামে ডাটা জমা রাখা।
যেমনঃ 
let x;
x = 6;
console.log(x);
*/ 

